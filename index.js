/**
 * This function adds two variable and returns its summation
 *
 * @param {number} num1 first number for summation
 * @param {number} num2 second number for summation
 * @returns {number} summation of the two number provided
 * 
 * 
 * @example 
 * 
 * addition (5,10)
 * 
 */

function addition (num1, num2) {
    return num1 + num2;
}

function multiplication (num1, num2) {
    return num1 * num2;
}